<template>
    <div>
        <input type="text" v-model="newTodoItem">
        <button @click="addTodo">추가</button>
    </div>
</template>

<script>
    export default {
        data() {
            return {
                newTodoItem : ''
            }
        },
        methods: {
            addTodo() {
                if(this.newTodoItem !== "") {
                    var things = this.newTodoItem && this.newTodoItem.trim();
                    localStorage.setItem(things, things);
                    this.clearInput();
                }
               
            },
            clearInput() {
                this.newTodoItem = '';
            }
        }
    }
</script>

